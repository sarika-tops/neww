package Basic;

public class UnaryOperators {
       public static void main(String[] args) {
    	      int i=2,j;
		      i++;
		      System.out.println(i);
		      j = i--;
		      System.out.println(j);
		      System.out.println(i);
		      
		      
		      int a = 10;
		      System.out.println(a);
		      int b = 15;
		      b += a;  //b = b+a;
		      System.out.println(b);
		      
	}
}
